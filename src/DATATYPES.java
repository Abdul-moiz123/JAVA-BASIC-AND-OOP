public class DATATYPES {
    public static void main(String[] args) {

        // DATATYPE:-

        // 1) THERE ARE TWO TYPE OD DATATYPES:-
        // 2) PRIMITIVE DATA --> INT,DOUBLE,SHORT,LONG,CHAR,BOOLEAN,FLOAT,BYTE
        // 3) NON-PRIMITIVE DATA--> STRING,ARRAY,CLASS

        // PRIMITIVE DATATYPE:-

        int a = 233443444;
        byte b = 100;
        short c = 12345;
        long d = 234345345;
        char e = 'a'; // 'A' OR 64 (ASCII CODE)
        float f = 2.345f;
        double g = 5.333333333d;
        boolean h = true; // false

        // NON-PRIMITIVE DATATYPE:-

        String moiz = "moiz";
        int [] moiz1 = {1,2,3,4,5,6,7};

        // SCENITIFIC NUMBER:-

        float f1 = 2.33e4f; // 2.330000
        float f2 = 2.33E4f; // 2.330000
        float f3 = 2.33e-4f; // 00002.33

        // octal and hexa to decimal:-

        int octal = 057;   // way to write to convert octal to decimal
        System.out.println(octal);
        int hexa =  0xA45;  // way to write to convert hexa to decimal
        System.out.println(hexa);

    }
}

public class TYPECASTING {
    public static void main(String[] args) {

        // TYPE CASTING:-

           // 1) THERE ARE TWO POSSIBLE WAYS FOR TYPE CASTING
           // 2) WIDENING METHOD --> SMALLER DATATYPE TO BIGGER DATATYPE
           // 2) NARROWING METHOD --> BIGGER DATATYPE TO SMALLER DATATYPE

        // WIDENING METHOD:-

        byte by = 23;
        int in = by;

        int num3 = 2345555;
        double num4 = num3;

        // NARROWING METHOD:-

        int num1 = 345;
        byte num2 = (byte) num1;

        double num5 = 23.4567;
        int num6 = (int) num5;


    }
}
